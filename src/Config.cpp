#include "../headers/AllHeaders.hpp"

Config::Config() 
	: server_port(0),
	server_name(""),
	root_dir(""),
	max_body_size(0),
	default_file(""),
	error_pages(),
	allowed_methods(),
	routes()
	{
	// std::cout << "Config default constructor called" << std::endl;
}

Config::Config(const Config &other) 
	: server_port(other.server_port),
	server_name(other.server_name),
	root_dir(other.root_dir),
	max_body_size(other.max_body_size),
	default_file(other.default_file),
	error_pages(other.error_pages),
	allowed_methods(other.allowed_methods),
	routes(other.routes) {
	// std::cout << "Config copy constructor called" << std::endl;
}

Config Config::operator=(const Config &another) {
	if (this == &another)
		return (*this);
	server_port = another.server_port;
	server_name = another.server_name;
	root_dir = another.root_dir;
	max_body_size = another.max_body_size;
	default_file = another.default_file;
	error_pages = another.error_pages;
	allowed_methods = another.allowed_methods;
	routes = another.routes;
	return (*this);
}

Config::~Config() {
	// std::cout << "Config destructor called" << std::endl;
}

void Config::printConfig() {
	std::cout << "\nPrinting config" << std::endl;
	std::cout << "server_port: " <<  server_port << std::endl;
	std::cout << "server_name: " << server_name << std::endl;
	std::cout << "root_dir: " << root_dir << std::endl;
	std::cout << "max_body_size: " << max_body_size << std::endl;
	std::cout << "default_file: " << default_file << std::endl;
	for (std::map<int, std::string>::iterator it = error_pages.begin(); it != error_pages.end(); ++it) {
		std::cout << "error_status: " << it->first << " error_file: " << it->second << std::endl;
	}
	for (unsigned long i = 0; i < allowed_methods.size(); i++) {
		std::cout << "allowed_methods: " << allowed_methods[i] << std::endl;
	}
	for (unsigned long i = 0; i < routes.size(); i++) {
		std::cout << "\nRoute " << i << std::endl;
		routes[i]->printRoute();
	}
}

Route	*Config::getRouteForTarget(const std::string &target) {
	Route	*route = NULL;
	for (unsigned long i = 0; i < routes.size(); i++) {
		if (routes[i]->containsTarget(target)) {
			if (!route)
				route = routes[i];
			else if (routes[i]->getPath().length() > route->getPath().length())
				route = routes[i];
		}
		else if (routes[i]->getPath() == getFileExtension(target)) {
			route = routes[i];
		}
	}
	return (route);
}

